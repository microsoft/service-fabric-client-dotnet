namespace Microsoft.ServiceFabric.Powershell.Http
{
    using System;
    using System.Collections.Generic;
    using System.Management.Automation;
    using Microsoft.ServiceFabric.Common;

    /// <summary>
    /// Cancels an image store upload session.
    /// </summary>
    [Cmdlet(VerbsCommon.Remove, "SFImageStoreUploadSession", DefaultParameterSetName = "DeleteImageStoreUploadSession")]
    public partial class RemoveImageStoreUploadSessionCmdlet : CommonCmdletBase
    {
        /// <summary>
        /// Gets or sets SessionId. A GUID generated by the user for a file uploading. It identifies an image store upload
        /// session which keeps track of all file chunks until it is committed.
        /// </summary>
        [Parameter(Mandatory = true, Position = 0, ParameterSetName = "DeleteImageStoreUploadSession")]
        public Guid? SessionId
        {
            get;
            set;
        }

        /// <summary>
        /// Gets or sets ServerTimeout. The server timeout for performing the operation in seconds. This timeout specifies the
        /// time duration that the client is willing to wait for the requested operation to complete. The default value for
        /// this parameter is 60 seconds.
        /// </summary>
        [Parameter(Mandatory = false, Position = 1, ParameterSetName = "DeleteImageStoreUploadSession")]
        public long? ServerTimeout
        {
            get;
            set;
        }

        /// <summary>
        /// Gets or sets the force flag. If provided, then the destructive action will be performed without asking for
        /// confirmation prompt.
        /// </summary>
        [Parameter(Mandatory = false, Position = 2, ParameterSetName = "DeleteImageStoreUploadSession")]
        public SwitchParameter Force
        {
            get;
            set;
        }

        /// <inheritdoc/>
        protected override void ProcessRecordInternal()
        {
            try
            {
                if (((this.Force != null) && this.Force) || this.ShouldContinue(string.Empty, string.Empty))
                {
                    this.ServiceFabricClient.ImageStore.DeleteImageStoreUploadSessionAsync(
                        sessionId: this.SessionId,
                        serverTimeout: this.ServerTimeout,
                        cancellationToken: this.CancellationToken).GetAwaiter().GetResult();

                    Console.WriteLine("Success!");
                }
            }
            catch (Exception ex)
            {
                Console.WriteLine(ex.Message);
            }
        }
    }
}
